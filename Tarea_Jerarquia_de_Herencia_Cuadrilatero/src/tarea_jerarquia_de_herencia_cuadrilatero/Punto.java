package tarea_jerarquia_de_herencia_cuadrilatero;

/**
 *
 * @author eliza
 */
public class Punto {

    private double cordenada_x;
    private double cordenada_y;

    public Punto(double cordenada_x, double cordenada_y) {
        this.cordenada_x = cordenada_x;
        this.cordenada_y = cordenada_y;
    }

    public double getCordenada_x() {
        return cordenada_x;
    }

    public double getCordenada_y() {
        return cordenada_y;
    }
}
